def apply_safety(coeffs, safety):
    if safety == 1:
        return [list(row) for row in coeffs]
    scaled = []
    for i, (a, b, c) in enumerate(coeffs):
        if i == len(coeffs) - 1:
            scaled.append([a, b, c])
        else:
            scaled.append([a / safety, b / safety**3, c / safety**5])
    return scaled


def optimal_quintic(lower, upper):
    if lower / upper >= 1 - 5e-6:
        return (15 / 8) / upper, (-10 / 8) / upper**3, (3 / 8) / upper**5

    q = (3 * lower + upper) / 4
    r = (lower + 3 * upper) / 4
    error = math.inf
    old_error = None
    while old_error is None or abs(old_error - error) > 1e-15:
        old_error = error
        lhs = np.array(
            [
                [lower, lower**3, lower**5, 1],
                [q, q**3, q**5, -1],
                [r, r**3, r**5, 1],
                [upper, upper**3, upper**5, -1],
            ],
            dtype=np.float64,
        )
        a, b, c, error = np.linalg.solve(lhs, np.ones(4, dtype=np.float64))
        discriminant = max(0.0, 9 * b**2 - 20 * a * c)
        roots = (-3 * b + np.array([-1, 1]) * math.sqrt(discriminant)) / (10 * c)
        q, r = np.sqrt(np.maximum(roots, 0))
    return float(a), float(b), float(c)


def optimal_composition(lower, initial_upper, steps, *, cushion, safety):
    upper = initial_upper
    coeffs = []
    for _ in range(steps):
        if lower > upper:
            lower = upper = (lower + upper) / 2
        a, b, c = optimal_quintic(max(lower, cushion * upper), upper)
        p_lower = a * lower + b * lower**3 + c * lower**5
        p_upper = a * upper + b * upper**3 + c * upper**5
        rescale = 2 / (p_lower + p_upper)
        a *= rescale
        b *= rescale
        c *= rescale
        coeffs.append((a, b, c))
        lower = a * lower + b * lower**3 + c * lower**5
        upper = 2 - lower
    return apply_safety(coeffs, safety)


def build_adaptive_coeffs():
    cache = {}
    schedules = []
    for lower in ADAPTIVE_LOWER_BOUNDS:
        key = round(lower, 10)
        if key not in cache:
            cache[key] = optimal_composition(
                lower,
                ADAPTIVE_UPPER_BOUND,
                ADAPTIVE_POLAR_STEPS,
                cushion=ADAPTIVE_CUSHION,
                safety=ADAPTIVE_SAFETY,
            )
        schedules.append(cache[key])
    return schedules


ADAPTIVE_POLAR_COEFFS = build_adaptive_coeffs()


def zeropower_via_coeff_tensor(G: Tensor, coeffs: Tensor) -> Tensor:
    assert G.ndim >= 2
    X = G.bfloat16()
    if G.size(-2) > G.size(-1):
        X = X.mT

    # Ensure spectral norm is at most 1
    X = X / (X.norm(dim=(-2, -1), keepdim=True) + 1e-7)
    for i in range(coeffs.size(0)):
        a, b, c = coeffs[i]
        A = X @ X.mT
        B = b * A + c * A @ A
        X = a * X + B @ X

    if G.size(-2) > G.size(-1):
        X = X.mT
    return X


@torch.compile
def muon_update(update, coeffs: Tensor):
    update = zeropower_via_coeff_tensor(update, coeffs)
    update *= max(1, update.size(-2) / update.size(-1))**0.5
    return update


class Muon(torch.optim.Optimizer):
    def __init__(self, params, lr=0.02, weight_decay=0, mu=0.95):
        assert isinstance(params, list) and len(params) >= 1 and isinstance(params[0], torch.nn.Parameter)
        params = sorted(params, key=lambda x: x.size(), reverse=True)
        defaults = dict(lr=lr, weight_decay=weight_decay, mu=mu)
        super().__init__(params, defaults)
        self.step_count = 0

    @torch.no_grad()
    def step(self):
        self.step_count += 1
        coeffs = torch.tensor(
            ADAPTIVE_POLAR_COEFFS[min(self.step_count, len(ADAPTIVE_POLAR_COEFFS)) - 1],
            device=device,
            dtype=torch.float32,
        )
        world_size = dist.get_world_size()
        rank = dist.get_rank()
        for group in self.param_groups:
            params = group["params"]
            params_pad = params + [torch.empty_like(params[-1])] * (world_size - len(params) % world_size)
            for base_i in range(0, len(params), world_size):
                if base_i + rank < len(params):
                    p = params[base_i + rank]
                    state = self.state[p]
                    if len(state) == 0:
                        state["momentum"] = torch.zeros_like(p)
                    state["momentum"].lerp_(p.grad, 1 - group["mu"])
                    update_input = p.grad.lerp(state["momentum"], group["mu"])
                    update = muon_update(update_input, coeffs)
                    p.mul_(1 - group["lr"] * group["weight_decay"])
                    p.add_(update, alpha=-group["lr"])
                dist.all_gather(params_pad[base_i:base_i + world_size], params_pad[base_i + rank])


